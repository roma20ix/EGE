def per(n, k):
    """
    Функция перевода натурального числа N в систему счисления с основанием K.

    В данной реализации K <= 10 и результат хранится в строке.
    Если же вам необходимо перевести N в СС с K > 10, то есть 2 варианта реализации:
        * Вместо строки хранить массив остатков (Лучше делать так)
        * В строку добавлять буквы из алфавита  (Так лучше не делать)

    Agrs: (int) N - число, которое необходимо перевести в СС с основанием K
          (int) K - основание СС
    Returns: (str) r - число N в СС с основанием K
    """

    r = ''
    while n:
        r = str(n % k) + r
        n //= k
    return r


