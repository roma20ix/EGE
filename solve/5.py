def f(n):
    n1 = per(n, 3)
    if n % 3 == 0:
        n1 += n1[-2:]
    else:
        n1 += per(sum(int(i) for i in n1) * 2, 3)
    return int(n1, 3)  # int(number, pos) - переводит number из СС с основанием pos в десятичную

