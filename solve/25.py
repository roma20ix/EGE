def divs(n):
    """
    Функция поиска всех делителей натурального числа N

    Идея поиска всех делителей основана на следующем факте:

        Делители числа всегда образуют пары: если d - делитель числа N, то
        существует парный ему делитель N/d, такой что:
                                    d * N/d = N

        При этом один из делителей в паре всегда <= sqrt(N), а другой >= sqrt(N)
        Доказательство этого факта остаётся читателю в качестве разминки

    Таким образом нам достаточно перебрать все числа до корня и выбрать из них делители
    
    Args: (int) N - натуральное число, делители которого необходимо найти
    Returns: (list[int]) d - итоговый массив с делителями числа N
    """
    d = []
    # Если в задаче необходимо найти нетривиальные делители, то есть не равные 1 и N, то первый параметр в цикле нужно поменять на 2
    for i in range(1, int(n ** 0.5) + 1):  # i <= sqrt(N)
        if n % i == 0:
            d.append(i)  # Делитель
            if i != n // i:
                # Проверка на то, что i - не корень из N (Пример N=36, i=6, N/i=6  =>  i=N/i)
                # Эта проверка необходима для того, чтобы дважды не добавлять корень из числа
                # Конечно можно не писать эту проверку, если вместо массива завести множество
                d.append(n // i)  # Парный делитель
    return d


