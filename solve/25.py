def fact(n):
    """
    Разложение натурального числа на простые множители (Факторизация)

    Вспомним, как мы это делали в 5 классе. Мы брали первое простое число и делили
    до тех пор, пока число делится. Дальше брали следующее простое число и делали тоже самое, до
    тех пор, пока наше число не станет равно 1, то есть больше ни на что не сможет поделиться.

    Рассмотрим пример:
    120 | 2
    60  | 2
    30  | 2
    15  | 3
    5   | 5
    1   | END

    120 = 2^3  *  3^1  *  5^1

    Отметим важное свойство, которое может быть полезным.
    Это свойство называется "Следствие из Основной Теоремы Арифметики", которое находит количество делителей.

    Пусть N = p1^a1  *  p2^a2  *  ...  *  pn^an
    Пусть T(N) - количество делителей числа N (Включая 1 и само число N)

    Тогда по следствию из Основной Теоремы Арифметики:
                                T(N) = (a1 + 1)  *  (a2 + 1)  *  ...  *  (an + 1)
    
    Args: (int) N - натуральное число, которое необходимо факторизовать
    Returns: (list[tuple[int]]) f - массив с разложением числа N
    """
    f = []
    p = 2
    while n > 1:
        if p*p > n: p = n  # Необходимо для правильного разложения, можете подумать тем, как это работает
        if n % p == 0:
            q = 0  # Степень вхождения простого числа p в разложение числа N
            while n % p == 0:
                q += 1
                n //= p
            f.append((p, q))  # p^q
        p += 1
    return f


