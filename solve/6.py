"""
Основные команды:
lt(angle) - поворот на angle градусов влево (против часовой стрелки)
rt(angle) - поворот на angle градусов вправо (по часовой стрелке)
fd(s)     - движение на s единиц вперёд
bk(s)     - движение на s единиц назад
tracer(x) - скорость отрисовки (0 - моментально)
screensize(w, h) - изменение размеров плоскости
goto(x, y) - перемещение в точку с координатами (x; y)
dot(r, color) - отрисовка точки радиуса r цвета color
"""


