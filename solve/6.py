k = 30                    # Коэффициент масштабирования (Не обязателен)

lt(90)                    # По умолчанию Черепашка направлена вдоль положительного направления оси абсцисс (ось OX), но по условию
                          #     она обычно должна быть направлению вдоль положительного направления ординат (ось OY)

# Далее прописываем алгоритм из условия

rt(45)
for _ in range(3):
    rt(45)
    fd(10*k)
    rt(45)
rt(315)
fd(10*k)
rt(90)
fd(20*k)
rt(90)
for _ in range(2):
    fd(10*k)
    rt(90)

# Теперь отрисуем точки на плоскости. Не будем особо задумываться о границах и возьмём побольше
up()  # Обязательно нужно поднять хвост, чтобы Черепашка не оставляла за собой лишних следов
for x in range(-20, 20):
    for y in range(-20, 20):
        goto(x*k, y*k)  # Перемещение в координату (x*k; y*k)
        dot(4, 'red')   # Отрисовка точки радиуса 4 красного цвета

update()                  # Для корректной отрисовки
